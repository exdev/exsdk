static inline float ex_radians ( float _radians ) { 
    if ( _radians > (float)EX_TWO_PI )
        return fmodf(_radians,(float)EX_TWO_PI); 
    else if ( _radians < 0.0f )
        return (float)EX_TWO_PI + fmodf(_radians,(float)EX_TWO_PI); 

    return _radians;
}

// ------------------------------------------------------------------ 
// Desc: 
// ------------------------------------------------------------------ 

static inline float ex_degree ( float _degrees ) {
    return ex_radians ( ex_deg2rad_f(_degrees) );
} 

// ------------------------------------------------------------------ 
// Desc: 
// ------------------------------------------------------------------ 

static inline float ex_radians_to_degrees_180 ( float _rad ) {
    float degrees = ex_rad2deg_f(_rad);
    return _rad >= (float)EX_PI ? degrees - 360.0f : degrees;
}

// ------------------------------------------------------------------ 
// Desc: 
// ------------------------------------------------------------------ 

static inline float ex_radians_to_degrees_360 ( float _rad ) { 
    return ex_rad2deg_f(_rad);
} 

// ------------------------------------------------------------------ 
// Desc: 
// ------------------------------------------------------------------ 

static inline void ex_radians_to_rot22 ( float _rad, ex_mat22f_t *_m ) {
    float cos_theta = cosf( _rad );
    float sin_theta = sinf( _rad );

    _m->m00 =  cos_theta, _m->m01 = sin_theta; 
    _m->m10 = -sin_theta, _m->m11 = cos_theta; 
} 

// ------------------------------------------------------------------ 
// Desc: 
// ------------------------------------------------------------------ 

static inline float ex_radians_neg ( float _rad ) { 
    return (float)EX_TWO_PI - _rad;
}

