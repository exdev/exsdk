const char * ex_str_replace ( const char *_path, char _from, char _to ) {
    char *p;

    p = (char *)_path;
    while ( *p != '\0' ) {
        if ( *p == _from ) {
            *p = _to;
        }
        ++p;
    }

    return _path;
}

