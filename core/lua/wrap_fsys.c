///////////////////////////////////////////////////////////////////////////////

#include "exsdk.h"

#include <lua.h>
#include <lauxlib.h>
#include <lualib.h>

///////////////////////////////////////////////////////////////////////////////
// functions
///////////////////////////////////////////////////////////////////////////////

// ------------------------------------------------------------------ 
// Desc: 
// ------------------------------------------------------------------ 

static int fsys_app_dir ( lua_State *_l ) {
    lua_pushstring( _l, ex_fsys_app_dir() );
    return 1;
}

// ------------------------------------------------------------------ 
// Desc: 
// ------------------------------------------------------------------ 

static int fsys_user_dir ( lua_State *_l ) {
    lua_pushstring( _l, ex_fsys_user_dir() );
    return 1;
}

// ------------------------------------------------------------------ 
// Desc: 
// ------------------------------------------------------------------ 

static const luaL_Reg lib[] = {
    { "fsys_app_dir",       fsys_app_dir },
    { "fsys_user_dir",      fsys_user_dir },
    { NULL, NULL }
};

int __ex_lua_add_fsys ( lua_State *_l ) {
    luaL_setfuncs( _l, lib, 0 );
    return 0;
}
