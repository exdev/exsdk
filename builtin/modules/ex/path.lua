local __M = {}

--/////////////////////////////////////////////////////////////////////////////
-- functions
--/////////////////////////////////////////////////////////////////////////////

-- ------------------------------------------------------------------ 
-- Desc: 
-- ------------------------------------------------------------------ 

local function translate ( _path, _sep )
    assert ( type(_path) == "string", "Type error: _path must be string" )

    if _sep == nil then
        if ex.platform == "windows" then
            _sep = "\\"
        else
            _sep = "/"
        end
    end
    return _path:gsub("[/\\]", _sep)
end
__M.translate = translate

--/////////////////////////////////////////////////////////////////////////////
-- 
--/////////////////////////////////////////////////////////////////////////////

return __M

