function string.findlast ( _s, _pattern, _plain )
    local curr = 0

    repeat
        local next = _s:find(_pattern, curr + 1, _plain)
        if next then curr = next end
    until not next

    if curr > 0 then
        return curr
    end	
end

-- ------------------------------------------------------------------ 
-- Desc: 
-- ------------------------------------------------------------------ 

function string.split ( _s, _pattern )
    local list = {}
    for w in _s:gmatch("[^".._pattern.."]+") do
        list[#list+1] = w
    end
    return list
end

-- ------------------------------------------------------------------ 
-- Desc: 
-- ------------------------------------------------------------------ 

function string.trim ( _s )
    return _s:match( "^%s*(.-)%s*$" )
end
