local __M = {}

--/////////////////////////////////////////////////////////////////////////////
-- base functions
--/////////////////////////////////////////////////////////////////////////////

-- ------------------------------------------------------------------ 
-- Desc: 
-- ------------------------------------------------------------------ 

function findlast ( _s, _pattern, _plain )
    local curr = 0

    repeat
        local next = _s:find(_pattern, curr + 1, _plain)
        if next then curr = next end
    until not next

    if curr > 0 then
        return curr
    end	
end
__M.findlast = findlast

--/////////////////////////////////////////////////////////////////////////////
-- 
--/////////////////////////////////////////////////////////////////////////////

return __M
