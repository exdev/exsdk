local __M = {}

--/////////////////////////////////////////////////////////////////////////////
-- base functions
--/////////////////////////////////////////////////////////////////////////////

-- ------------------------------------------------------------------ 
-- Desc: 
-- ------------------------------------------------------------------ 

local builtin_error = error
local function error ( _msg, ... )
    print(debug.traceback())
    builtin_error(_msg, ...)
end
__M.error = error

-- ------------------------------------------------------------------ 
-- Desc: 
-- ------------------------------------------------------------------ 

local builtin_assert = assert
local function assert ( _exp, ... )
    if _exp == false then print(debug.traceback()) end
    builtin_assert(_exp, ...)
end
__M.assert = assert

--/////////////////////////////////////////////////////////////////////////////
-- 
--/////////////////////////////////////////////////////////////////////////////

return __M
