local __M = {}

--/////////////////////////////////////////////////////////////////////////////
-- base functions
--/////////////////////////////////////////////////////////////////////////////

-- ------------------------------------------------------------------ 
-- Desc: 
-- ------------------------------------------------------------------ 

local function contains (_t,_v)
    for _,v in pairs(_t) do
        if v == _v then
            return true
        end
    end
    return false
end
__M.contains = contains

-- ------------------------------------------------------------------ 
-- Desc: 
-- ------------------------------------------------------------------ 

local function copy ( _to, _from )
    for k,v in pairs(_from) do
        _to[k] = v
    end
    return _to
end
__M.copy = copy

-- ------------------------------------------------------------------ 
-- Desc: 
-- ------------------------------------------------------------------ 

local function add ( _to, _el )
    _to[#_to+1] = _el
    return _to
end
__M.add = add

-- ------------------------------------------------------------------ 
-- Desc: 
-- ------------------------------------------------------------------ 

local function add_range ( _to, _list )
    assert ( type(_list) == "table" and #_list > 0, "Invalid _list" )
    for i=1,#_list do
        _to[#_to+1] = _list[i]
    end

    return _to
end
__M.add_range = add_range

--/////////////////////////////////////////////////////////////////////////////
-- 
--/////////////////////////////////////////////////////////////////////////////

return __M
