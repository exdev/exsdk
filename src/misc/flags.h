static inline uint32 ex_flags_add ( uint32 _src, uint32 _flags ) { 
    return _src | _flags;
} 

//
static inline uint32 ex_flags_remove ( uint32 _src, uint32 _flags ) {
    return _src & ~_flags;
}

//
static inline bool ex_flags_has_all ( uint32 _src, uint32 _flags ) {
    return (_flags == 0) ? (_src == _flags) : (_src&_flags) == _flags; 
}

//
static inline bool ex_flags_has ( uint32 _src, uint32 _flags ) {
    return ( _src & _flags ) != 0; 
}

